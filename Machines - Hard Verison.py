def max_machines_with_budget(T, test_cases):
    results = []
    
    for i in range(T):
        R, C, D = test_cases[i][0]
        NR, NC, ND = test_cases[i][1]
        PR, PC, PD = test_cases[i][2]
        N = test_cases[i][3]
        
        def can_support(machines):
            extra_ram = max(0, machines * R - NR)
            extra_cpu = max(0, machines * C - NC)
            extra_disk = max(0, machines * D - ND)
            cost = extra_ram * PR + extra_cpu * PC + extra_disk * PD
            return cost <= N
        
        low, high = 0, 10**9
        while low < high:
            mid = (low + high + 1) // 2
            if can_support(mid):
                low = mid
            else:
                high = mid - 1
        
        results.append(low)
    
    return results

